    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int rowNumber = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (rowNumber == -1) {
            JOptionPane.showMessageDialog(this, "no hay seleccionada fila");
        } else {
            Persona p = new Persona();
            p.setNombre((String) jTable1.getModel().getValueAt(rowNumber, 0));
            p.setApellidos((String) jTable1.getModel().getValueAt(rowNumber, 1));
            p.setTelefono((String) jTable1.getModel().getValueAt(rowNumber, 2));

            if (lista.contains(p)) {
                lista.remove(p);
                model.removeRow(rowNumber);
                a.writeListaPersonas(lista);
                JOptionPane.showMessageDialog(this, "Persona " + p.getNombre()+ " eliminada");
            } else {
                System.err.println("Esto no deberia de pasar: los valores de la tabla vienen fijados en el archivo y la "
                        + "tabla no es editable, no obstante el valor Persona seleccionada  no esta "
                        + "en la  lista: " + p.toString());
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

