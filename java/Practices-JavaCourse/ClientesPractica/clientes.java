    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        String valor= jTextField1.getText();
        if (clientes != null && !clientes.isEmpty() && !jTextField1.getText().equals("")) {
            for (cliente elemento : clientes) {
                if(elemento.getId().equals(valor)) {
                    jPasswordField1.setText(elemento.getPass());
                    jPasswordField2.setText(elemento.getPass());
                    jRadioButton1.setSelected(elemento.isEsPremium());
                    jRadioButton2.setSelected(elemento.isEsPremium());
                    break;
                }
            }           
        }else{
             JOptionPane.showMessageDialog(this, "Error buscar, lista vacia o campo de texto vacio");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

