    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean validoId= false;
        boolean validoPass = false;
        boolean validoPremium = false;

        cliente cliente = new cliente();
        String password = new String(jPasswordField1.getPassword());
        String password2 = new String(jPasswordField2.getPassword());
        
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo Id vacio");
            validoId = false;
        } else {
            cliente.setId(jTextField1.getText());
            validoId = true;
        }

        if (jPasswordField1.getPassword().length == 0 || jPasswordField2.getPassword().length == 0) {
            JOptionPane.showMessageDialog(this, "Campo password vacio");
            validoPass = false;
        } else if (password.equals(password2)) {
            cliente.setPass(password.toString());
            validoPass = true;
        }else{
            JOptionPane.showMessageDialog(this, "password no coincideb");
        }

        if (jRadioButton1.isSelected()) {
            cliente.setEsPremium(false);
            validoPremium= true;
        } else {
            cliente.setEsPremium(true);
        }

        if (jRadioButton2.isSelected()) {
            cliente.setEsPremium(true);
            validoPremium= true;
        } else {
            cliente.setEsPremium(false);         
        }
        
        if(validoId == true && true == validoPass && validoPremium == true){
            clientes.add(cliente);
            JOptionPane.showMessageDialog(this, "cliente agregado");
            System.out.print(cliente.esPremium);
            System.out.print(cliente.id);
            System.out.print(cliente.pass);
            System.out.print(clientes.size());
        }else{
            JOptionPane.showMessageDialog(this, "Error: No se ha guardado ningun valor");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

