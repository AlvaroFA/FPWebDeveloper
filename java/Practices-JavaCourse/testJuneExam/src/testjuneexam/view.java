    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Alumn a = new Alumn();

        if (jTextField1.equals("") | jTextField2.equals("") | jTextField3.equals("") | jComboBox1.getSelectedItem().toString().equals("Curso")) {
            JOptionPane.showMessageDialog(this, "Por favor rellene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            a.setName(jTextField1.getText());
            a.setSurName(jTextField2.getText());
            a.setDni(jTextField3.getText());
            a.setCourse(jComboBox1.getSelectedItem().toString());
            if (alumnList.contains(a)) {
                alumnList.remove(a);
                JOptionPane.showMessageDialog(this, "Persona " + a.getName() + " eliminada de la lista.");
            } else {
                JOptionPane.showMessageDialog(this, "La persona"+a.getDni()+ "no se encuentra en el fichero");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (alumnList.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay datos", "Error lista", JOptionPane.ERROR_MESSAGE);
        } else {
            String[] courses = {"1º", "2º", "3º", "4º", "5º", "6º"};
            JFrame frame = new JFrame("popup");
            JTextArea ta = new JTextArea(25, 25);
            JScrollPane scrollPane = new JScrollPane(ta);
            ta.setEditable(false);
            
            String courseSelected = (String) JOptionPane.showInputDialog(frame,
                    "Seleccione curso",
                    "Cursos",
                    JOptionPane.DEFAULT_OPTION,
                    null,
                    courses,
                    courses[0]);
            if(courseSelected == null){
                JOptionPane.showMessageDialog(this, "No hay datos", "Error lista", JOptionPane.ERROR_MESSAGE);
            }
            
            for (Alumn a : alumnList) {
                if (a.getCourse().equals(courseSelected)) {
                    ta.setText("nombre" + "\t" + "apellidos" + "\t" + "DNI" + "\n" + a.getName() + "\t" + a.getSurName() + "\t" + a.getDni());
                }
            }
            JOptionPane.showMessageDialog(frame, scrollPane);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

