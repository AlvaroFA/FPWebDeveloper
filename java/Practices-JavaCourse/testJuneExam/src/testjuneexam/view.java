    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed

        if (jTextField1.getText().equals("") && jTextField2.getText().equals("") && jTextField3.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Sin Criterios de busqueda,defina al menos 1");
        } else {
            String nom = jTextField1.getText();
            String ape = jTextField2.getText();
            String dni = jTextField3.getText();
            String course = jComboBox1.getSelectedItem().toString();
            boolean encontrado = false;
            for (Alumn al : alumnList) {
                if ((!nom.isEmpty() && al.getName().contains(nom))
                        || (!ape.isEmpty() && al.getSurName().contains(ape))
                        || (!dni.isEmpty() && al.getDni().contains(dni))
                        || (course.equals("Curso")&& al.getCourse().contains(course))
                        )
                {
                    encontrado = true;
                    JOptionPane.showMessageDialog(this,"nombre:" + al.getName()+"\n" + "apellidos:" + al.getSurName()+ "\n" + "DNI:"+al.getDni()+"\n"+"Course: "+al.getCourse(),"Resultado",JOptionPane.INFORMATION_MESSAGE);
                } else {
                    encontrado = false;
                }
            }
            if (encontrado == false) {
                JOptionPane.showMessageDialog(this, "sin resultados");
            }

        }
    }//GEN-LAST:event_jButton7ActionPerformed

