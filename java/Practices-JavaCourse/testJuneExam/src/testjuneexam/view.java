    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Pattern DNi = Pattern.compile("[XYxy]?[0-9]{1,9}[A-Za-z]");
        Matcher mDNI = DNi.matcher(jTextField3.getText().toUpperCase());

        Alumn a = new Alumn();
        String valorcombo = jComboBox1.getSelectedItem().toString();

        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Name is empty");
            checkName = false;
        } else {
            a.setName(jTextField1.getText());
            checkName = true;
        }
        if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Surname is empty");
            checkSurname = false;
        } else {
            a.setSurName(jTextField2.getText());
            checkSurname = true;
        }

        if (valorcombo.equals("Curso")) {
            JOptionPane.showMessageDialog(this, "Select course");
            checkCourse = false;
        } else {
            a.setCourse(jComboBox1.getSelectedItem().toString());
            checkCourse = true;
        }

        if (mDNI.matches()) {
            a.setDni(jTextField3.getText());
            checkDni = true;
        } else {
            JOptionPane.showMessageDialog(this, "Formato de DNI incorrecto.", "Error",
                    JOptionPane.ERROR_MESSAGE);
            checkDni = false;
        }

        boolean encontrado = false;
        for (Alumn al : alumnList) {
            if (al.getDni().equals(a.getDni())) {
                encontrado = true;
                break;
            }
        }
        if (encontrado) {
            JOptionPane.showMessageDialog(this, "ya existe");

        } else {
            if (checkCourse == true && checkName == true && checkSurname == true && checkDni == true) {
                alumnList.add(a);
                JOptionPane.showMessageDialog(this, "Alumn added to list");

            } else {
                JOptionPane.showMessageDialog(this, "No se ha insertado al alumno", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

